	<h3>Món ngon Dìn Ký</h3>

	<p>Nhà hàng Dìn Ký Nguyễn Trãi luôn nỗ lực không ngừng thay đổi bản thân, để khi quý thực khách nhớ đến, chúng tôi không chỉ là một địa điểm với các món ăn hấp dẫn, mà còn là nơi lý tưởng để trải nghiệm sự tinh túy của văn hóa ẩm thực đến từ khắp mọi miền.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-11.jpeg" alt="" width="50%" height="50%">
	
	
	<h1>1. CHẢ GIÒ DÌN KÝ</h1>
	<p>Từ xưa hay có câu “Nem công, chả phượng”, từng khoanh chả giò với vỏ giòn rụm, nhân ruột thơm ngon, đậm đà lấp đầy bởi hải sản, tôm mực và sò điệp tươi ngon xắt hạt lựu, hòa quyện cùng sốt hải sản trứ danh của Dìn Ký. Nhân bên trong là hải sản luôn tươi mới và được cắt thành từng miếng nhỏ để lưu giữ vị ngon nguyên bản và cảm giác chân thực nhất. Món này ăn kèm với sốt hải sản thơm béo độc quyền của Dìn Ký là tuyệt hảo, đảm bảo bạn sẽ nhớ mãi.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-12.jpeg" alt="" width="50%" height="50%">


	<h1>2. TÔM HÙM BABY SỐT TIÊU ĐEN</h1>
	<p>Hải sản chắc chắn là một thành viên không thể thiếu trong gia đình ẩm thực, đặc biệt tại đây, nhà hàng nằm trong top 10 hải sản tươi sống, Dìn Ký luôn chọn lọc và kiểm kê an toàn thực phẩm rõ ràng, khách hàng có thể tự tay lựa và cảm nhận “hải sản tươi sống tại hồ”. Chúng tôi sử dụng tôm hùm baby tươi ngon nhất được trang bị từ hồ nuôi rộng hải sản đúng chuẩn, đảm bảo hải sản tươi ngon mới bán. Tôm hùm baby kết hợp cùng sốt tiêu đen để át đi vị tanh hải sản, tạo nên hương vị thơm nồng, tăng vị ngon và kích thích vị giác. Khi ăn, bạn sẽ cảm nhận được hương vị của vùng biển khơi từ tôm hùm, độ giòn sần sật của sợi gân xen kẻ thịt săn chắc, kèm theo các sớ thịt ăn đến đâu ngon đến đó.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-13.jpeg" alt="" width="50%" height="50%">


	<h1>3. CÁ BỐNG MÚ HẤP HỒNG KÔNG</h1>
	<p>Chúng tôi dùng cá bống mú tươi mới, được hấp và thưởng thức ngay tại bàn với công nghệ hấp làm chín thực phẩm bằng hơi nước, bảo toàn vitamin và khoáng chất trong cá, bảo đảm hàm lượng dinh dưỡng cao cùng vệ sinh an toàn thực phẩm. Ngoài ra, chúng tôi không sử dụng dầu mỡ trong chế biến, đảm bảo bữa ăn không Cholesterol, không sử dụng nhiều gia vị tẩm ướp, cho món ăn luôn giữ hương vị nguyên bản. Với hương vị thơm ngon, thịt cá được hấp dai, nước sốt được rưới đều lên mình cá cùng một ít hành, gừng sợi, ớt và rau thơm cho món ăn thêm màu sắc và tăng phần hấp dẫn..</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-14.jpeg" alt="" width="50%" height="50%">


	<h1>4. GIÒ HEO MUỐI CHIÊN GIÒN</h1>
	<p>Với món này, giò heo sẽ được chế biến theo cách mới toanh, cắt thành từng lát mỏng sau đó chiên giòn vàng đều với độ canh lửa chuẩn xác, được ăn kèm với củ kiệu, cải chua và nước chấm mắm chanh, ớt. Với lớp da bên ngoài dai giòn, vàng ươm, bên trong thịt chân giò mềm mềm, đặc sệt sẽ tạo nên hương vị khó quên cho thực khách.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-15.jpeg" alt="" width="50%" height="50%">


	<h1>5. MIẾN XÀO CUA NGUYÊN CON TAY CẦM</h1>
	<p>Miến xào cua tay cầm là sự kết hợp giữa những sợi miến mềm dai và thịt cua Cà Mau tươi được xào cùng các loại gia vị đặc trưng của nhà hàng. Điểm tạo nên sự hấp dẫn lớn nhất của món ăn này là thịt cua tươi được để nguyên con giúp người ăn cảm nhận được hết vị ngọt của thịt hòa quyện vào từng sợi miến nóng hổi. Khi ăn, bạn không phải tự tay bóc vỏ cua mà sẽ được nhân viên hỗ trợ tách vỏ cua ra giúp bữa ăn thuận tiện và nhanh chóng hơn.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-16.jpeg" alt="" width="50%" height="50%">


	<h1>6. HEO LÊN MẸT</h1>
	<p>Heo rừng với thớ thịt săn chắc, ít mỡ, đủ dai đủ mềm, thịt heo được thái miếng vừa ăn, từng miếng thịt với phần da mỏng chín, giòn đều. Một phần heo lên mẹt được thiết kế hợp lý cho khoảng 3-4 người ăn. Thịt mềm, dồi dai dai, heo giòn sần sật, rau thơm, thêm chút cay nồng của ớt, sả cây, chua chua chát chát của lá cóc, chấm kèm với mắm mằn mặn dậy lên hương vị đặc trưng của từng thành phần. Từng miếng thịt bóng bẩy được đặt trên tàu lá xanh mát, đánh thức cả thị giác, khứu giác lẫn vị giác của mọi thực khách.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-17.jpeg" alt="" width="50%" height="50%">


	<h1>7. LẨU CÁ TẦM KHOAI MÔN</h1>
	<p>Nước lẩu thanh ngọt tự nhiên, khoai môn vừa béo vừa bùi thoảng mùi thơm của khoai, thêm một chút vị chao làm kích thích vị giác. Thử một miếng cá tầm, thịt cá vừa ngọt vừa tươi, ngấm vừa đủ các loại gia vị mang lại cảm giác khó quên trên đầu lưỡi. Lẩu này phải ăn với mì, những sợi mì vàng óng ánh được làm nguyên chất từ khoai tây, dai dai, quyện vào nước lẩu thì đúng thật là mỹ vị nhân gian.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-18.jpeg" alt="" width="50%" height="50%">


	<h1>8. GỎI BÒ NƯỚNG MÈ</h1>
	<p>Gỏi bò nướng rau thơm ngon miệng lạ thường do bắp bò nướng thơm nồng kết hợp cùng vị thơm lừng từ mè rang chín, vị chua dịu của tắc, vị đậm đà của nước trộn gỏi và rau thơm thiên nhiên, chỉ cần rưới nước chấm, trộn lên một xíu là đậm đà ngay, thấm vị từng miếng thịt, ăn kèm với rau là hết sẩy chắc chắn sẽ tạo nên hương vị độc đáo, hòa quyện hài hòa, lưu giữ vị thơm lâu cho thực khách lần đầu thưởng thức</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-19.jpeg" alt="" width="50%" height="50%">


	<h1>9. HEO MỌI NƯỚNG GIẢ CHỒN</h1>
	<p>Ăn kèm theo thịt heo mọi nướng giả chồn là lá mơ, ớt và một chút rau tía tô cùng chén nước mắm nguyên chất vắt thêm chút chanh. Vị thơm nồng của thịt kết hợp với lớp da giòn tan, vàng đều, vị bùi của lá thơm và hương nồng của hành lá tươi chong quyện trong cái vị đậm đà hơi cay của thức chấm làm cho món ăn dân dã này trở nên ngon khó tả, ăn một lần là nhớ mãi.</p>
	<img src="http://dinkynguyentrai.com.vn/wp-content/uploads/2020/03/word-image-20.jpeg" alt="" width="50%" height="50%">


	<h1>10. MÌ UDON XÀO SÒ ĐIỆP</h1>
	<p>Mì udon của Nhật Bản có nhiều cách để thưởng thức và đặc biệt hấp dẫn khi kết hợp cùng sò điệp tươi. Khi ăn bạn sẽ cảm nhận được mì dai mềm thấm gia vị xào, thịt bò mềm vừa chín tới, không gây dai cùng với rau củ như cà rốt và khoai tây băm vẫn giữ được độ giòn ngon, rau thơm và nấm kim châm cùng tô điểm thêm đầy màu sắc. Quả là món ngon tiện lợi không thể bỏ qua trong dịp này.</p>
