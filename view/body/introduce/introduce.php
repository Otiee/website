      <h2>HÃY ĐẾN VỚI CHÚNG TÔI ĐỂ ĐƯỢC PHỤC VỤ TỐT NHẤT</h2>
      <p>Dìn Ký Nguyễn Trãi Hơn 28 năm phục vụ những thực khách Sài Thành sành ăn, Dìn Ký Nguyễn Trãi (DKNT) – một thành viên của Dìn Ký Group – đã trở thành một điểm đến quen thuộc khi nhắc đến Ẩm thực Sài Gòn (Nay là TP.HCM).</p>
      <p>Nhà hàng Dìn Ký Hoạt động liên tục 24/7, do đó vào thời điểm nào trong ngày, thực khách DKNT đều có thể thoả sức lựa chọn từ hơn 400 món ăn NGON – LÀNH được chế biến công phu, kết hợp hài hoà phong cách của ẩm thực Âu-Á-Việt-Hoa.</p>
      <p>Nhắc đến DKNT, người sành ăn sẽ nghĩ ngay đến món như hủ tiếu cá, mì kéo sợi hay điểm tâm Hồng Kông vào buổi sáng; cơm Việt Nam vào buổi trưa; các món ngon tuyệt hảo được chế biến từ thủy hải sản tươi sống vào buổi tối.</p>

      <h4>Chất lượng tuyệt hảo</h4>      
      <h6>Menu đa dạng</h6>
      <p>Vào những năm 1990, cùng với sự phát triển năng động của nền kinh tế, TP. HCM đã trở thành điểm đến của nhiều cư dân trong nước và du khách quốc tế nên nhu cầu cẩm thực cũng trở nên đa dạng. Sau 15 năm xây dựng và phát triển, bằng những nỗ lực không ngừng, DÌN KÝ đã trở thành một thương hiệu nổi bật trên thị trường ẩm thực Việt Nam với hơn 300 món ăn Âu – Á – Việt – Hoa độc đáo và hấp dẫn. Cũng với những cái tên rất đỗi quen thuộc như: Cà ri gà Ấn Độ, Thịt cua đút lò, Salad Nga, Trứng Cá Hồi Đen, Lẩu Thái Lan, Heo sữa quay Trung Hoa… nhưng dưới bàn tay tài hoa của những đầu bếp DÌN KÝ, món ăn đó trở nên hấp dẫn đến kỳ lạ. Bởi khi chế biến các món ăn đó, DÌN KÝ không thực hiện một cách máy móc theo những công thức có sẵn mà đã nghiên cứu tăng – giảm nhiều nguyên vật liệu để món ăn mang đặc trưng riêng của mình.</p>

      <h4>Phục vụ chuyên nghiệp</h4>
      <h6>CHẤT LƯỢNG DỊCH VỤ HÀNG ĐẦU</h6>
      <p>Dìn Ký là một trong những thương hiệu uy tín, gắn bó lâu năm với thực khách Sài Gòn. Chính niềm đam mê ẩm thực cùng nỗ lực không ngừng nghỉ trong việc mang đến cho quý khách hàng những trải nghiệm đáng nhớ, không gian gần gũi cùng dịch vụ chất lượng, Dìn Ký luôn nằm trong số các địa điểm thú vị khi tổ chức tiệc cưới, thôi nôi, liên hoan, sinh nhật…</p>
      <p>Với đội ngũ quản lý giàu kinh nghiệm, nhân viên chuyên nghiệp, đầu bếp nhiều năm kinh nghiệm, Nhà hàng Dìn Ký ngày càng cố gắng và hoàn thiện chất lượng để mang lại cho khách hàng những dịch vụ tốt.</p>

      <h4>An toàn thực phẩm</h4>
      <h6>NGUYÊN LIỆU ĐẠT CHUẨN</h6>
      <p>Bên cạnh những món ăn có hương vị ngon tuyệt thì đội ngũ đầu bếp Nhà hàng Dìn Ký luôn luôn làm ẩm thực bằng cả tấm lòng, với tinh thần trách nhiệm cao, chính vì vậy, các món ăn đều được chế biến từ nguồn thực phẩm tươi, sạch, đảm bảo cho sức khỏe và sự an tâm tuyệt đối cho khách hàng.</p>

      <h4>Không gian tuyệt vời</h4>
      <h6>CƠ SỞ VẬT CHẤT HIỆN ĐẠI</h6>
      <p>Với lối thiết kế mở, hầu hết ở các nhà hàng Dìn Ký thực khách có thể dễ dàng bắt gặp màu xanh của cây cối, sắc trắng của mây trời hay tận hưởng không khí trong lành, thoáng đãng ở khu vực ngoài trời, bỏ lại hết những ồn ào, bon chen của cuộc sống thường nhật khi dùng bữa cùng gia đình, bạn bè tại đây.</p>
      <p>Bên cạnh đó, Nhà hàng Dìn Ký còn sở hữu những sảnh tiệc với cở sở vật chất hiện đại, đầy đủ các thiết bị âm thanh ánh sáng hiện đại để phục vụ nhiều loại hình dịch vụ khác nhau như tiệc cưới, thôi nôi, sinh nhật, hội nghị….

